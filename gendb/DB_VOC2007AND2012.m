function [  cid2name, ...
            iid2impath, ...
            iid2size, ...
            iid2setid, ...
            oid2cid, ...
            oid2diff, ...
            oid2iid, ...
            oid2bbox, ...
            oid2cont ] = DB_VOC2007AND2012
    [  cid2name, ...
        iid2impath07, ...
        iid2size07, ...
        iid2setid07, ...
        oid2cid07, ...
        oid2diff07, ...
        oid2iid07, ...
        oid2bbox07, ...
        oid2cont07 ] = DB_VOC2007;
    [  ~, ...
        iid2impath12, ...
        iid2size12, ...
        iid2setid12, ...
        oid2cid12, ...
        oid2diff12, ...
        oid2iid12, ...
        oid2bbox12, ...
        oid2cont12 ] = DB_VOC2012;
    iid2impath = cat( 1, iid2impath07, iid2impath12 );
    iid2size = cat( 2, iid2size07, iid2size12 );
    iid2setid = cat( 1, iid2setid07 ./ iid2setid07, iid2setid12 );
    oid2cid = cat( 1, oid2cid07, oid2cid12 );
    oid2diff = cat( 1, oid2diff07, oid2diff12 );
    oid2iid = cat( 1, oid2iid07, oid2iid12 + numel( iid2impath07 ) );
    oid2bbox = cat( 2, oid2bbox07, oid2bbox12 );
    oid2cont = cat( 1, oid2cont07, oid2cont12 );
end